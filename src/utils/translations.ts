export function translate(key: string, language: string): string {
  const langTranslations = translations[language as keyof typeof translations] || translations.en;
  return (langTranslations as any)[key] || (translations.en as any)[key] || key;
}

export function useTranslations(language: string) {
  const t = (key: string) => translate(key, language);
  return {
    t,
    translations: translations[language as keyof typeof translations] || translations.en,
  };
}
